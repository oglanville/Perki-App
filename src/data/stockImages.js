/* Stable per-perk pick so the same perk always shows the same photo. */
const hash = (str) => { let h = 5381; for (let i = 0; i < (str || "").length; i++) h = ((h << 5) + h + str.charCodeAt(i)) >>> 0; return h; };
const pick = (arr, seed) => arr[hash(seed) % arr.length];

export const categoryImage = (category, seed = "") => {
  const pool = CATEGORY_POOLS[category];
  return pool ? pick(pool, seed || category) : null;
};

export const perkImage = (perk) => {
  if (!perk) return null;
  if (perk.image_url) return perk.image_url;
  const text = `${perk.provider || ""} ${perk.title || ""} ${perk.description || ""}`;
  for (const [re, imgs] of KEYWORD_RULES) {
    if (re.test(text)) return pick(imgs, perk.perk_id || perk.title);
  }
  return categoryImage(perk.category, perk.perk_id || perk.title);
};

/* Back-compat single-image map (first of each pool). */
export const CATEGORY_IMAGE = Object.fromEntries(Object.entries(CATEGORY_POOLS).map(([k, v]) => [k, v[0]]));
